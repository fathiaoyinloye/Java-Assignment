public class Thing{
	protected String name;
	private double weight;


	public Thing(double weight){
		this.weight = weight;


	}

	public void setName(String name){
		this.name = name;

	}

	public String getName(){
		return name;

	}
	public void setWeight(double weight){
		this.weight = weight;


	}
	public double getWeight(){
		return weight;

	}
	 


}